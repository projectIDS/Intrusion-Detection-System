#!/bin/bash

file="ip_details.txt"

while IFS= read -r line
do
	echo "==============================================================================="
	echo ".....................Unblocking Attacker IP --> "$line".................."
	echo "==============================================================================="
	iptables -D INPUT -s "$line" -j DROP
    service iptables save
	echo ".........................IP Address UnBlocked!!................................"
	echo "..............................Rectified!!......................................"
	echo "==============================================================================="
done <"$file"
